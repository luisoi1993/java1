/*Ciclo do while

    do{
        Inatrucciones;
      }while(condicion);
 */
package ciclo_do_while;


public class Ciclo_do_while {

   
    public static void main(String[] args) {
        int i=1;
        
        do{
            System.out.println(i);
            i++;
        }while(i<=10);
    }
    
}
